/*
|------------------------------------------------------------------
| Typing Context
|------------------------------------------------------------------
*/

function isTypingTarget(
    target,
)
{
    if (!target) {
        return false;
    }

    /*
    |--------------------------------------------------------------
    | Content editable
    |--------------------------------------------------------------
    */

    if (
        target.isContentEditable
    ) {
        return true;
    }

    /*
    |--------------------------------------------------------------
    | Native inputs
    |--------------------------------------------------------------
    */

    return [
        'INPUT',
        'TEXTAREA',
        'SELECT',
    ].includes(
        target.tagName,
    );
}

/*
|------------------------------------------------------------------
| Init
|------------------------------------------------------------------
*/

export function initBackNavigation()
{
    /*
    |--------------------------------------------------------------
    | Anti double init
    |--------------------------------------------------------------
    */

    if (
        document.body.dataset
            .backNavigationInit
        === 'true'
    ) {
        return;
    }

    document.body.dataset
        .backNavigationInit =
            'true';

    /*
    |--------------------------------------------------------------
    | Keyboard back
    |--------------------------------------------------------------
    */

    document.addEventListener(
        'keydown',
        event =>
        {
            /*
            |------------------------------------------------------
            | Backspace only
            |------------------------------------------------------
            */

            if (
                event.key
                !== 'Backspace'
            ) {
                return;
            }

            /*
            |------------------------------------------------------
            | Ignore typing fields
            |------------------------------------------------------
            */

            if (
                isTypingTarget(
                    event.target,
                )
            ) {
                return;
            }

            /*
            |------------------------------------------------------
            | Ignore modifiers
            |------------------------------------------------------
            */

            if (
                event.ctrlKey
                || event.metaKey
                || event.altKey
                || event.shiftKey
            ) {
                return;
            }

            /*
            |------------------------------------------------------
            | Prevent browser weird behavior
            |------------------------------------------------------
            */

            event.preventDefault();

            /*
            |------------------------------------------------------
            | Back navigation
            |------------------------------------------------------
            */

            if (
                window.history.length > 1
            ) {

                window.history.back();

                return;
            }

            /*
            |------------------------------------------------------
            | Fallback
            |------------------------------------------------------
            */

            window.location.href =
                '/lolissr/manga';
        },
    );
}